INITIAL_N = 24
GROWTH_STEPS = 40
COUPLED_SAMPLES = 250

ENDOGENOUS_KERNELS = (
    "principal_ideal",
    "two_ideal_union",
    "random_downset",
    "history_reinforced",
)

TRANSFORMED_MODELS = (
    "power_anisotropic",
    "beta_skew",
)

EXOGENOUS_BETA = 7.0
RANDOM_DOWNSET_SEED_PROBABILITY = 0.16

MIN_EXOGENOUS_DIVERGENCE_RATE = 0.90
MAX_MEDIAN_FIRST_DIVERGENCE_STEP = 10.0


def json_safe(value):
    if isinstance(value, dict):
        return {
            str(key): json_safe(item)
            for key, item in value.items()
        }
    if isinstance(value, (list, tuple)):
        return [json_safe(item) for item in value]
    if isinstance(value, np.bool_):
        return bool(value)
    if isinstance(value, np.integer):
        return int(value)
    if isinstance(value, np.floating):
        return float(value)
    return value


def apply_model(
    model: str,
    u: np.ndarray,
    v: np.ndarray,
) -> tuple[np.ndarray, np.ndarray]:
    if model == "uniform":
        return u, v

    if model == "power_anisotropic":
        return np.power(u, 4.0), np.sqrt(v)

    if model == "beta_skew":
        return (
            beta.ppf(u, 2.0, 5.0),
            beta.ppf(v, 5.0, 2.0),
        )

    raise ValueError(f"Unknown model: {model}")


def transform_mark(
    model: str,
    u: np.ndarray,
) -> np.ndarray:
    if model == "uniform":
        return u.copy()

    if model == "power_anisotropic":
        return np.power(u, 4.0)

    if model == "beta_skew":
        return beta.ppf(u, 2.0, 5.0)

    raise ValueError(f"Unknown model: {model}")


def relation_matrix(
    x: np.ndarray,
    y: np.ndarray,
) -> np.ndarray:
    relation = (
        (x[:, None] < x[None, :])
        & (y[:, None] < y[None, :])
    )
    np.fill_diagonal(relation, False)
    return relation


def relation_hash(relation: np.ndarray) -> str:
    return hashlib.sha256(
        np.packbits(
            relation.astype(np.uint8),
            axis=None,
        ).tobytes()
    ).hexdigest()


def path_hash(
    step_hashes: list[str],
) -> str:
    return hashlib.sha256(
        "|".join(step_hashes).encode("utf-8")
    ).hexdigest()


def is_transitive(
    relation: np.ndarray,
) -> bool:
    two_step = (
        relation.astype(np.int16)
        @ relation.astype(np.int16)
    ) > 0
    return not bool(
        np.any(two_step & ~relation)
    )


def categorical_choice(
    weights: np.ndarray,
    uniform_value: float,
) -> int:
    weights = np.asarray(weights, dtype=float)

    if np.any(weights < 0.0):
        raise ValueError("Categorical weights must be nonnegative.")

    total = float(weights.sum())
    if not math.isfinite(total) or total <= 0.0:
        raise ValueError("Categorical weights have invalid total.")

    target = min(
        uniform_value,
        np.nextafter(1.0, 0.0),
    ) * total
    index = int(
        np.searchsorted(
            np.cumsum(weights),
            target,
            side="right",
        )
    )
    return min(index, len(weights) - 1)


def principal_ideal(
    relation: np.ndarray,
    anchor: int,
) -> np.ndarray:
    downset = relation[:, anchor].copy()
    downset[anchor] = True
    return downset


def add_maximal_vertex(
    relation: np.ndarray,
    downset: np.ndarray,
) -> np.ndarray:
    n = len(relation)
    updated = np.zeros(
        (n + 1, n + 1),
        dtype=bool,
    )
    updated[:n, :n] = relation
    updated[:n, n] = downset
    return updated


def build_random_tape(
    rng: np.random.Generator,
) -> dict[str, np.ndarray]:
    maximum_n = INITIAL_N + GROWTH_STEPS

    return {
        "anchor_1": rng.random(GROWTH_STEPS),
        "anchor_2": rng.random(GROWTH_STEPS),
        "subset": rng.random(
            (GROWTH_STEPS, maximum_n)
        ),
        "new_mark_latent": rng.random(GROWTH_STEPS),
    }


def run_endogenous_kernel(
    initial_relation: np.ndarray,
    kernel: str,
    tape: dict[str, np.ndarray],
) -> dict[str, object]:
    relation = initial_relation.copy()
    step_hashes = [relation_hash(relation)]
    selected_actions: list[str] = []
    transitive_every_step = is_transitive(relation)

    selection_counts = np.zeros(
        len(relation),
        dtype=np.int64,
    )

    for step in range(GROWTH_STEPS):
        n = len(relation)
        past_counts = relation.sum(
            axis=0
        ).astype(float)
        future_counts = relation.sum(
            axis=1
        ).astype(float)

        if kernel == "principal_ideal":
            weights = 1.0 + past_counts
            anchor = categorical_choice(
                weights,
                float(tape["anchor_1"][step]),
            )
            downset = principal_ideal(
                relation,
                anchor,
            )
            action = f"a:{anchor}"

        elif kernel == "two_ideal_union":
            first_anchor = categorical_choice(
                1.0 + past_counts,
                float(tape["anchor_1"][step]),
            )
            second_anchor = categorical_choice(
                1.0 + future_counts,
                float(tape["anchor_2"][step]),
            )
            downset = (
                principal_ideal(
                    relation,
                    first_anchor,
                )
                | principal_ideal(
                    relation,
                    second_anchor,
                )
            )
            action = (
                f"a1:{first_anchor}|a2:{second_anchor}"
            )

        elif kernel == "random_downset":
            seed_vertices = (
                tape["subset"][step, :n]
                < RANDOM_DOWNSET_SEED_PROBABILITY
            )
            downset = seed_vertices.copy()

            for seed in np.flatnonzero(
                seed_vertices
            ):
                downset |= principal_ideal(
                    relation,
                    int(seed),
                )

            action = (
                "seeds:"
                + ",".join(
                    str(int(vertex))
                    for vertex in np.flatnonzero(
                        seed_vertices
                    )
                )
            )

        elif kernel == "history_reinforced":
            weights = (
                1.0
                + past_counts
                + selection_counts.astype(float)
            )
            anchor = categorical_choice(
                weights,
                float(tape["anchor_1"][step]),
            )
            downset = principal_ideal(
                relation,
                anchor,
            )
            selection_counts[anchor] += 1
            action = (
                f"a:{anchor}|count:"
                f"{selection_counts[anchor]}"
            )

        else:
            raise ValueError(
                f"Unknown endogenous kernel: {kernel}"
            )

        relation = add_maximal_vertex(
            relation,
            downset,
        )
        selection_counts = np.append(
            selection_counts,
            0,
        )
        selected_actions.append(action)
        step_hashes.append(
            relation_hash(relation)
        )
        transitive_every_step &= is_transitive(
            relation
        )

    return {
        "final_relation": relation,
        "step_hashes": step_hashes,
        "path_hash": path_hash(step_hashes),
        "actions": selected_actions,
        "action_hash": hashlib.sha256(
            "|".join(
                selected_actions
            ).encode("utf-8")
        ).hexdigest(),
        "transitive_every_step": (
            transitive_every_step
        ),
    }


def run_exogenous_mark_kernel(
    initial_relation: np.ndarray,
    initial_marks: np.ndarray,
    model: str,
    tape: dict[str, np.ndarray],
) -> dict[str, object]:
    relation = initial_relation.copy()
    marks = initial_marks.copy()
    step_hashes = [relation_hash(relation)]
    selected_anchors: list[int] = []
    transitive_every_step = is_transitive(relation)

    for step in range(GROWTH_STEPS):
        centered = marks - float(marks.max())
        weights = np.exp(
            EXOGENOUS_BETA * centered
        )
        anchor = categorical_choice(
            weights,
            float(tape["anchor_1"][step]),
        )
        downset = principal_ideal(
            relation,
            anchor,
        )
        relation = add_maximal_vertex(
            relation,
            downset,
        )

        latent_new_mark = np.asarray(
            [tape["new_mark_latent"][step]],
            dtype=float,
        )
        transformed_new_mark = transform_mark(
            model,
            latent_new_mark,
        )[0]
        marks = np.append(
            marks,
            transformed_new_mark,
        )

        selected_anchors.append(anchor)
        step_hashes.append(
            relation_hash(relation)
        )
        transitive_every_step &= is_transitive(
            relation
        )

    return {
        "final_relation": relation,
        "step_hashes": step_hashes,
        "path_hash": path_hash(step_hashes),
        "anchors": selected_anchors,
        "anchor_hash": hashlib.sha256(
            np.asarray(
                selected_anchors,
                dtype=np.int32,
            ).tobytes()
        ).hexdigest(),
        "transitive_every_step": (
            transitive_every_step
        ),
    }


def first_divergence_step(
    first_hashes: list[str],
    second_hashes: list[str],
) -> int | None:
    for index, (
        first_hash,
        second_hash,
    ) in enumerate(
        zip(first_hashes, second_hashes)
    ):
        if first_hash != second_hash:
            # Index zero is the initial state.
            return index
    return None


