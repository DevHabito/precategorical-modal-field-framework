"""
A21 — Equivariance of Endogenous Relational Dynamics

Exact theorem
-------------
Let O_t be the complete observed relational history through step t. Let the
next state be generated by

    O_{t+1} = Phi_t(O_0,...,O_t, Xi_t),

where Xi_t is fresh model-independent randomness and Phi_t has access only to
the observed relational history and endogenous state measurable from it.

If two latent models are related by a transformation that leaves O_0
identical, then under the coupling that uses the same Xi_t at every step,

    O_t^(1) = O_t^(2)  for every t

by induction. Hence their trajectory laws are identical. This covers Markov
and history-dependent kernels, deterministic or stochastic.

Computational audit
-------------------
Initial orders are generated from the same latent (U,V) under three marginal
representatives:
    uniform;
    X=U^4, Y=sqrt(V);
    X=BetaPPF(U;2,5), Y=BetaPPF(V;5,2).

Four endogenous growth kernels are tested:
    1. principal-ideal anchor;
    2. union of two principal ideals;
    3. random seed set followed by downward closure;
    4. history-reinforced principal-ideal anchor.

All use the same random tape across coupled representatives.

Positive control
----------------
A fifth kernel selects its anchor with probabilities proportional to

    exp(beta * Z_i),

where Z is an externally calibrated quantitative mark. Under different
marginal representatives, Z changes even when the order does not. This kernel
is expected to break trajectory identity.

Boundary
--------
The positive control is a mathematical witness, not a proposed physical
dynamics. A pass proves a symmetry restriction on order-endogenous kernels;
it does not identify a fundamental law or a physical clock.
"""

